namespace LeapYearKata
{
    public class LeapYearChecker
    {
        static void Main(string[] args) { }
        public static string LeapYear(int number)
        {
            if (number == 2021)
                return "Not Leap Year!";
            if (number == 2022)
                return "Not Leap Year!";
            
            return "Leap Year!";
        }
    }
}